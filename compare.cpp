// comparison operators are used to compare two values or variables 
#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 3;
    cout << (x > y); // it will return 1 (true) because 5 is greater than 3
}

// operators 
// == Equal to X == Y
// != Not eqaul to X != Y
// > Greater than  X > Y
// < Less than X < Y
// >= Greater than or equal to X >= Y
// <= Less than or equal to X <= Y